package Array;

class Declaration_Instantiation_Initialization
{
    public static void main(String args[]) {
        
        // Declaration (declaration only)
        int[] arr1;

        // Instantiation (declaration and instantiation)
        int[] arr2 = new int[5]; // allocated memory to an array of 5 integers.

        // Initialization (declaration, instantiation and initialization)
        int[] arr3 = {10, 20, 30, 40, 50};
    }
}